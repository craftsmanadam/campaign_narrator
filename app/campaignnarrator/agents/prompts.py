    "guards at the east gate', 'Talia found a hidden door behind the "
    "bookcase'). On failure, leave state_effects empty — the failure "
    "consequence is narrated, not state-recorded. "
    "For all other knowledge checks and social rolls that do not change HP, "
    "inventory, phase, or encounter outcome, leave state_effects empty.\n\n"
    "VALID state_effect types (use ONLY these):\n"
