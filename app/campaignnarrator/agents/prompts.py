PLAYER_INTENT_INSTRUCTIONS: str = (
    "Classify the player's input into exactly one IntentCategory and return a "
    "PlayerIntent.\n\n"
    "CATEGORY DEFINITIONS:\n"
    "- hostile_action: The player is declaring a present-tense attack, hostile act, "
    "or use of a weapon against a creature. Examples: 'I charge the goblin', "
    "'I fire an arrow', 'I cast Fireball at the guards', 'I stab it'.\n"
    "- skill_check: Any action with an uncertain outcome that requires a D&D 5e skill "
    "check. Examples: 'I try to sneak past the guard' (Stealth), 'I pick the lock' "
    "(Sleight of Hand), 'I persuade the guard' (Persuasion), 'I examine the runes' "
    "(Investigation), 'I recall lore about undead' (Arcana or Religion).\n"
    "- npc_dialogue: The player is speaking to or addressing an NPC in a way that "
    "carries no uncertain outcome — asking questions, stating facts, casual "
    "conversation. Examples: 'I ask the innkeeper about the bandits', "
    "'I greet the guard', 'I introduce myself to the merchant'.\n"
    "- scene_observation: The player is moving, looking, or narrating without "
    "triggering a skill check or NPC conversation. Examples: 'I walk to the door', "
    "'I look around the room', 'I approach the gate'.\n"
    "- save_exit: The player wants to save state and leave the session. "
    "Examples: 'save and exit', 'save and leave', 'quit', 'exit the game'.\n"
    "- status: The player is querying their current status. "
    "Examples: 'status', 'how am I doing', 'what are my HP'.\n"
    "- recap: The player wants a summary of recent events. "
    "Examples: 'what happened', 'recap', 'what have we done'.\n"
    "- look_around: The player wants a description of current surroundings. "
    "Examples: 'look around', 'what do I see', 'describe the scene'.\n\n"
    "PRIORITY RULE — hostile_action vs skill_check: hostile_action requires an "
    "explicit physical attack declaration against a named or specific creature: "
    "'I attack the goblin', 'I fire an arrow at the guard', 'I cast Fireball at "
    "the group'. Drawing or readying a weapon as part of a social act "
    "(intimidation, show of force, threatening posture) is skill_check "
    "(Intimidation), not hostile_action. The presence of a weapon does not make "
    "the action hostile — the declared intent does. Examples: 'I draw my sword to "
    "frighten them' → skill_check; 'I raise my shield to draw their attention' → "
    "skill_check; 'I stab the goblin' → hostile_action.\n\n"
    "PRIORITY RULE — skill_check vs npc_dialogue: When the input could be either, "
    "always prefer skill_check. Any statement directed at an NPC that attempts to "
    "influence their behavior (threaten, intimidate, persuade, deceive, bluff, "
    "charm, bargain, bribe) is skill_check, regardless of how it is phrased. "
    "npc_dialogue is only for informational exchanges with no uncertain outcome.\n\n"
    "PRESENT-TENSE RULE: Only classify as hostile_action when the player is "
    "declaring a *current* act. Past-tense recounting ('We attacked the goblins "
    "yesterday') is scene_observation, not hostile_action.\n\n"
    "check_hint RULE: Set check_hint to the standard D&D 5e skill name when "
    "category is skill_check. It must be null for all other categories. "
    "Example skill names: Stealth, Athletics, Persuasion, Intimidation, "
    "Deception, Investigation, Arcana.\n\n"
    "reason: One sentence used for logging only — never shown to the player.\n\n"
    "OUTPUT FORMAT:\n"
    "Return exactly three fields: category, check_hint, reason.\n"
    "Do NOT include any other fields. Do NOT echo back phase, actor_summaries, "
    "recent_events, setting, player_input, or any other input fields.\n"
    "Do NOT use 'phase' as a field name — the output field is 'category'.\n"
    "Do NOT nest the skill name inside a sub-object — set check_hint directly "
    'to the skill name string (e.g. check_hint: "Intimidation"), or null.\n'
    "Do not narrate or resolve rules."
)
